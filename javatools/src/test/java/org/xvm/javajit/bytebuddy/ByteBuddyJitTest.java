    @Test
    @DisplayName("ByteBuddy - Basic Class Generation")
    void testBasicClassGeneration() throws Exception {
        LOGGER.info("--- Test: Basic Class Generation ---");
        
        // Generate a simple class with ByteBuddy
        String className = "org.xvm.test.GeneratedByByteBuddy";
        LOGGER.info("Generating class: " + className);
        
        DynamicType.Unloaded<?> unloaded = byteBuddy
            .subclass(Object.class)
            .name(className)
            .defineMethod("getMessage", String.class)
            .intercept(FixedValue.value("Hello from ByteBuddy!"))
            .make();
        
        // Get bytecode
        byte[] bytecode = unloaded.getBytes();
        LOGGER.info("Generated bytecode size: " + bytecode.length + " bytes");
        
        // Load the class
        Class<?> generatedClass = unloaded.load(getClass().getClassLoader()).getLoaded();
        LOGGER.info("Loaded class: " + generatedClass.getName());
        LOGGER.info("Superclass: " + generatedClass.getSuperclass().getName());
        
        // Test the generated class
        Object instance = generatedClass.getDeclaredConstructor().newInstance();
        Method method = generatedClass.getMethod("getMessage");
        String result = (String) method.invoke(instance);
        
        LOGGER.info("Method invocation result: " + result);
        
        assertEquals("Hello from ByteBuddy!", result);
        assertEquals(className, generatedClass.getName());
        assertEquals(Object.class, generatedClass.getSuperclass());
        
        LOGGER.info("✓ Basic class generation test passed");
    }
    
    @Test
    @DisplayName("ByteBuddy - Bytecode Transform Test")
    void testBytecodeTransform() throws Exception {
        LOGGER.info("--- Test: Bytecode Transform ---");
        
        // Create a class that we'll transform
        String originalClassName = "org.xvm.test.OriginalClass";
        String transformedClassName = "org.xvm.test.TransformedClass";
        
        LOGGER.info("Creating original class: " + originalClassName);
        
        // First, create an original class
        DynamicType.Unloaded<?> originalType = byteBuddy
            .subclass(Object.class)
            .name(originalClassName)
            .defineMethod("getValue", int.class)
            .intercept(FixedValue.value(42))
            .make();
        
        byte[] originalBytecode = originalType.getBytes();
        LOGGER.info("Original class bytecode size: " + originalBytecode.length + " bytes");
        
        // Now create a transformed version
        LOGGER.info("Creating transformed class: " + transformedClassName);
        
        DynamicType.Unloaded<?> transformedType = byteBuddy
            .subclass(Object.class)
            .name(transformedClassName)
            .defineMethod("getValue", int.class)
            .intercept(FixedValue.value(99))  // Different value
            .defineMethod("getTransformed", boolean.class)
            .intercept(FixedValue.value(true))  // Additional method
            .make();
        
        byte[] transformedBytecode = transformedType.getBytes();
        LOGGER.info("Transformed class bytecode size: " + transformedBytecode.length + " bytes");
        
        // Load and test both classes
        Class<?> originalClass = originalType.load(getClass().getClassLoader()).getLoaded();
        Class<?> transformedClass = transformedType.load(getClass().getClassLoader()).getLoaded();
        
        Object originalInstance = originalClass.getDeclaredConstructor().newInstance();
        Object transformedInstance = transformedClass.getDeclaredConstructor().newInstance();
        
        // Test original class
        Method originalGetValue = originalClass.getMethod("getValue");
        int originalValue = (int) originalGetValue.invoke(originalInstance);
        LOGGER.info("Original class getValue(): " + originalValue);
        
        // Test transformed class
        Method transformedGetValue = transformedClass.getMethod("getValue");
        int transformedValue = (int) transformedGetValue.invoke(transformedInstance);
        LOGGER.info("Transformed class getValue(): " + transformedValue);
        
        Method getTransformed = transformedClass.getMethod("getTransformed");
        boolean isTransformed = (boolean) getTransformed.invoke(transformedInstance);
        LOGGER.info("Transformed class getTransformed(): " + isTransformed);
        
        // Verify the transform worked
        assertEquals(42, originalValue);
        assertEquals(99, transformedValue);
        assertTrue(isTransformed);
        
        // Verify bytecode is different
        assertFalse(java.util.Arrays.equals(originalBytecode, transformedBytecode));
        LOGGER.info("✓ Bytecode sizes differ: original=" + originalBytecode.length + 
                   ", transformed=" + transformedBytecode.length);
        
        LOGGER.info("✓ Bytecode transform test passed");
    }
    
