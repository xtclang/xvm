    @Test
    @DisplayName("ByteBuddy - Resource Loading Simulation")
    void testResourceLoadingSimulation() throws Exception {
        LOGGER.info("--- Test: Resource Loading Simulation ---");
        
        // Simulate what would happen in a real resource loading scenario
        String resourceClassName = "org.xvm.test.ResourceHandler";
        LOGGER.info("Creating resource handler class: " + resourceClassName);
        
        DynamicType.Unloaded<?> resourceType = byteBuddy
            .subclass(Object.class)
            .name(resourceClassName)
            .defineMethod("loadResource", String.class)
                .withParameter(String.class, "resourceName")
            .intercept(FixedValue.value("Resource loaded successfully"))
            .defineMethod("getResourceInfo", String.class)
            .intercept(FixedValue.value("ByteBuddy Resource Handler v1.0"))
            .make();
        
        byte[] bytecode = resourceType.getBytes();
        LOGGER.info("Resource handler bytecode size: " + bytecode.length + " bytes");
        
        // Simulate storing bytecode as if it were a resource
        LOGGER.info("Simulating bytecode storage and retrieval...");
        
        // Load from "resource" (bytecode array)
        Class<?> resourceClass = resourceType.load(getClass().getClassLoader()).getLoaded();
        Object resourceHandler = resourceClass.getDeclaredConstructor().newInstance();
        
        Method loadResource = resourceClass.getMethod("loadResource", String.class);
        Method getResourceInfo = resourceClass.getMethod("getResourceInfo");
        
        String loadResult = (String) loadResource.invoke(resourceHandler, "test.properties");
        String info = (String) getResourceInfo.invoke(resourceHandler);
        
        LOGGER.info("loadResource('test.properties') result: " + loadResult);
        LOGGER.info("getResourceInfo() result: " + info);
        
        assertEquals("Resource loaded successfully", loadResult);
        assertEquals("ByteBuddy Resource Handler v1.0", info);
        
        LOGGER.info("✓ Resource loading simulation test passed");
    }
    
    @Test
    @DisplayName("ByteBuddy - Complex Class Structure")
    void testComplexClassStructure() throws Exception {
        LOGGER.info("--- Test: Complex Class Structure ---");
        
        String className = "org.xvm.test.ComplexGeneratedClass";
        LOGGER.info("Creating complex class: " + className);
        
        // Create a more complex class structure
        DynamicType.Unloaded<?> complexType = byteBuddy
            .subclass(Object.class)
            .name(className)
            // Multiple methods with different signatures
            .defineMethod("getString", String.class)
            .intercept(FixedValue.value("ByteBuddy Complex Class"))
            .defineMethod("getNumber", int.class)
            .intercept(FixedValue.value(42))
            .defineMethod("getBoolean", boolean.class)
            .intercept(FixedValue.value(true))
            .defineMethod("processData", String.class)
                .withParameter(String.class, "input")
                .withParameter(int.class, "count")
            .intercept(FixedValue.value("Processed"))
            // Define fields (simulated)
            .defineMethod("getFieldValue", String.class)
            .intercept(FixedValue.value("field_value"))
            .make();
        
        byte[] bytecode = complexType.getBytes();
        LOGGER.info("Complex class bytecode size: " + bytecode.length + " bytes");
        
        // Load and thoroughly test
        Class<?> complexClass = complexType.load(getClass().getClassLoader()).getLoaded();
        Object instance = complexClass.getDeclaredConstructor().newInstance();
        
        // Test all methods
        Method getString = complexClass.getMethod("getString");
        Method getNumber = complexClass.getMethod("getNumber");
        Method getBoolean = complexClass.getMethod("getBoolean");
        Method processData = complexClass.getMethod("processData", String.class, int.class);
        Method getFieldValue = complexClass.getMethod("getFieldValue");
        
        String stringResult = (String) getString.invoke(instance);
        int numberResult = (int) getNumber.invoke(instance);
        boolean booleanResult = (boolean) getBoolean.invoke(instance);
        String processedResult = (String) processData.invoke(instance, "test", 5);
        String fieldResult = (String) getFieldValue.invoke(instance);
        
        LOGGER.info("getString(): " + stringResult);
        LOGGER.info("getNumber(): " + numberResult);
        LOGGER.info("getBoolean(): " + booleanResult);
        LOGGER.info("processData('test', 5): " + processedResult);
        LOGGER.info("getFieldValue(): " + fieldResult);
        
        // Verify all results
        assertEquals("ByteBuddy Complex Class", stringResult);
        assertEquals(42, numberResult);
        assertTrue(booleanResult);
        assertEquals("Processed", processedResult);
        assertEquals("field_value", fieldResult);
        
        // Verify method count
        Method[] methods = complexClass.getDeclaredMethods();
        LOGGER.info("Total methods in complex class: " + methods.length);
        assertTrue(methods.length >= 5, "Should have at least 5 methods");
        
        LOGGER.info("✓ Complex class structure test passed");
    }
