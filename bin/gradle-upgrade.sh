#!/usr/bin/env bash
#
# Upgrade Gradle wrappers and repo-local Gradle version mirrors for xvm and,
# optionally, the sibling xtclang repos.
#
# This script intentionally owns more than gradle/wrapper/gradle-wrapper.properties:
# historical Gradle upgrade PRs have also needed regenerated wrapper artifacts,
# XtcProjectCreator's fallback Gradle version, and embedded-Kotlin comments.

set -euo pipefail

usage() {
    cat <<'EOF'
Usage:
  bin/gradle-upgrade.sh [gradle-version] [--skip-build-validation] [--allow-dirty]
  bin/gradle-upgrade.sh [gradle-version] --repo=<repo> [--skip-build-validation]
  bin/gradle-upgrade.sh [gradle-version] --all-xtclang-repos [--skip-build-validation]
  bin/gradle-upgrade.sh --check [--repo=<repo>|--all-xtclang-repos]

Examples:
  bin/gradle-upgrade.sh
  bin/gradle-upgrade.sh <gradle-version>
  bin/gradle-upgrade.sh <gradle-version> --repo=platform
  bin/gradle-upgrade.sh <gradle-version> --all-xtclang-repos
  bin/gradle-upgrade.sh --check --all-xtclang-repos

When no version is provided, the script resolves the current stable Gradle
version from https://services.gradle.org/versions/current.

What upgrade mode does:
  1. Refuses to edit any target repo on master or main.
  2. Refuses downgrades.
  3. Runs the Gradle wrapper task twice for each stale wrapper root so
     properties, wrapper jar, and scripts are generated by the target Gradle.
  4. Rewrites gradle-wrapper.properties using each wrapper's existing policy:
     services.gradle.org, -bin distribution, timeout, retries, URL validation.
  5. Updates XtcProjectCreator.DEFAULT_GRADLE_VERSION when present.
  6. Updates the build-logic comment that records Gradle's embedded Kotlin
     when present.
  7. Fails if old exact Gradle versions remain in tracked files.
  8. Runs focused validation unless --skip-build-validation is used.

What --all-xtclang-repos does:
  Includes the current xvm checkout plus sibling checkouts named platform,
  xtc-app-template, and examples under the same parent directory. Nested
  wrapper roots are included too, such as examples/CardGame. For each repo
  that needs an upgrade, this mode creates a gradle-upgrade-<version> branch,
  upgrades and validates all selected repos, then commits and pushes each
  upgrade branch to origin. Tracked working tree changes are not allowed in
  this mode.

What --repo=<repo> does:
  Selects one xtclang repo and uses the same branch, commit, and push behavior
  as --all-xtclang-repos. Supported names are xvm, platform,
  xtc-app-template, and examples.

What --check does:
  Checks target repo metadata without changing files.
EOF
}

fail() {
    echo "ERROR: $*" >&2
    exit 1
}

info() {
    echo "==> $*" >&2
}

mode="upgrade"
target_version=""
skip_build_validation=false
allow_dirty=false
all_xtclang_repos=false
selected_repo_name=""

while [[ $# -gt 0 ]]; do
    case "$1" in
        --check)
            mode="check"
            shift
            ;;
        --all-xtclang-repos)
            all_xtclang_repos=true
            shift
            ;;
        --repo=*)
            selected_repo_name="${1#--repo=}"
            [[ -n "$selected_repo_name" ]] || fail "--repo requires a repo name"
            shift
            ;;
        --repo)
            shift
            [[ $# -gt 0 ]] || fail "--repo requires a repo name"
            selected_repo_name="$1"
            shift
            ;;
        --skip-build-validation)
            skip_build_validation=true
            shift
            ;;
        --allow-dirty)
            allow_dirty=true
            shift
            ;;
        -h|--help)
            usage
            exit 0
            ;;
        --)
            shift
            break
            ;;
        -*)
            fail "Unknown option: $1"
            ;;
        *)
            if [[ -n "$target_version" ]]; then
                fail "Unexpected extra argument: $1"
            fi
            target_version="$1"
            shift
            ;;
    esac
done

repo_root=$(git rev-parse --show-toplevel)
cd "$repo_root"

uses_upgrade_branches=false
if $all_xtclang_repos || [[ -n "$selected_repo_name" ]]; then
    uses_upgrade_branches=true
fi

if $all_xtclang_repos && [[ -n "$selected_repo_name" ]]; then
    fail "--repo and --all-xtclang-repos are mutually exclusive"
fi

if [[ "$selected_repo_name" == */* || "$selected_repo_name" == "." || "$selected_repo_name" == ".." ]]; then
    fail "--repo expects one repo name, not a path"
fi

if $uses_upgrade_branches && $allow_dirty; then
    fail "--allow-dirty is not supported with --repo or --all-xtclang-repos because those modes commit and push changes"
fi

validate_version_argument() {
    local version="$1"
    [[ -n "$version" ]] || fail "Gradle version is required"
    [[ "$version" != *[[:space:]]* ]] || fail "Gradle version must not contain whitespace"
    [[ "$version" =~ ^[0-9]+\.[0-9]+\.[0-9]+$ ]] || fail "Use a stable numeric Gradle version in major.minor.patch form"
}

resolve_latest_gradle_version() {
    local payload version

    command -v curl >/dev/null 2>&1 || fail "curl is required to resolve the latest Gradle version"

    info "Resolving latest stable Gradle version"
    payload=$(curl -fsSL https://services.gradle.org/versions/current) ||
        fail "Could not resolve latest Gradle version from services.gradle.org"
    version=$(printf '%s\n' "$payload" | sed -n 's/.*"version"[[:space:]]*:[[:space:]]*"\([^"]*\)".*/\1/p' | head -1)
    [[ -n "$version" ]] || fail "Could not parse latest Gradle version from services.gradle.org response"

    printf '%s\n' "$version"
}

compare_gradle_versions() {
    local left="$1"
    local right="$2"
    local left_major left_minor left_patch right_major right_minor right_patch

    IFS=. read -r left_major left_minor left_patch <<< "$left"
    IFS=. read -r right_major right_minor right_patch <<< "$right"

    if (( left_major < right_major )); then
        printf '%s\n' -1
    elif (( left_major > right_major )); then
        printf '%s\n' 1
    elif (( left_minor < right_minor )); then
        printf '%s\n' -1
    elif (( left_minor > right_minor )); then
        printf '%s\n' 1
    elif (( left_patch < right_patch )); then
        printf '%s\n' -1
    elif (( left_patch > right_patch )); then
        printf '%s\n' 1
    else
        printf '%s\n' 0
    fi
}

repo_name() {
    basename "$1"
}

repo_relative() {
    local repo="$1"
    local path="$2"
    local prefix="$repo/"
    printf '%s\n' "${path#"$prefix"}"
}

read_property_file() {
    local file="$1"
    local key="$2"
    awk -F= -v key="$key" '$1 == key {print substr($0, index($0, "=") + 1)}' "$file" | tail -1
}

wrapper_version_from_props() {
    local props="$1"
    sed -n 's|^distributionUrl=.*gradle-\(.*\)-bin\.zip.*|\1|p' "$props" | tail -1
}

gradle_root_for_props() {
    local props="$1"
    printf '%s\n' "${props%/gradle/wrapper/gradle-wrapper.properties}"
}

gradle_line_for_version() {
    local version="$1"
    local major minor

    major=$(printf '%s' "$version" | sed -n 's/^\([0-9][0-9]*\).*/\1/p')
    minor=$(printf '%s' "$version" | sed -n 's/^[0-9][0-9]*\.\([0-9][0-9]*\).*/\1/p')
    if [[ -n "$major" && -n "$minor" ]]; then
        printf '%s.%s.x' "$major" "$minor"
    else
        printf '%s' "$version"
    fi
}

contains_line() {
    local haystack="$1"
    local needle="$2"
    grep -Fxq "$needle" <<< "$haystack"
}

append_unique_line() {
    local haystack="$1"
    local needle="$2"

    if [[ -z "$needle" ]]; then
        printf '%s' "$haystack"
    elif [[ -z "$haystack" ]]; then
        printf '%s\n' "$needle"
    elif contains_line "$haystack" "$needle"; then
        printf '%s' "$haystack"
    else
        printf '%s\n%s\n' "$haystack" "$needle"
    fi
}

count_lines() {
    local text="$1"
    if [[ -z "$text" ]]; then
        printf '%s\n' 0
    else
        printf '%s\n' "$text" | sed '/^$/d' | wc -l | tr -d ' '
    fi
}

discover_wrapper_props() {
    local repo="$1"
    git -C "$repo" ls-files |
        grep -E '(^|/)gradle/wrapper/gradle-wrapper.properties$' |
        while IFS= read -r rel; do
            printf '%s/%s\n' "$repo" "$rel"
        done
}

resolve_named_repo() {
    local name="$1"
    local parent_dir candidate current_name

    parent_dir=$(cd "$repo_root/.." && pwd)
    current_name=$(repo_name "$repo_root")

    if [[ "$name" == "xvm" || "$name" == "$current_name" ]]; then
        candidate="$repo_root"
    else
        case "$name" in
            platform|xtc-app-template|examples)
                candidate="$parent_dir/$name"
                ;;
            *)
                fail "Unknown xtclang repo '$name'; use xvm, platform, xtc-app-template, or examples"
                ;;
        esac
    fi

    [[ -d "$candidate/.git" ]] || fail "Repo '$name' was not found at $candidate"
    printf '%s\n' "$candidate"
}

discover_target_repos() {
    local parent_dir candidate seen
    seen=""

    if [[ -n "$selected_repo_name" ]]; then
        resolve_named_repo "$selected_repo_name"
        return
    fi

    if ! $all_xtclang_repos; then
        printf '%s\n' "$repo_root"
        return
    fi

    parent_dir=$(cd "$repo_root/.." && pwd)

    for candidate in \
        "$repo_root" \
        "$parent_dir/platform" \
        "$parent_dir/xtc-app-template" \
        "$parent_dir/examples"
    do
        [[ -d "$candidate/.git" ]] || fail "Required xtclang repo not found at $candidate"
        if ! contains_line "$seen" "$candidate"; then
            seen=$(append_unique_line "$seen" "$candidate")
            printf '%s\n' "$candidate"
        fi
    done
}

current_branch() {
    git -C "$1" branch --show-current
}

upgrade_branch_name() {
    local version="$1"
    printf 'gradle-upgrade-%s\n' "$version"
}

ensure_not_protected_branch() {
    local repo="$1"
    local branch

    branch=$(current_branch "$repo")
    [[ -n "$branch" ]] || fail "$(repo_name "$repo") is in detached HEAD; refusing to edit"

    case "$branch" in
        master|main)
            fail "$(repo_name "$repo") is on protected branch '$branch'; create an upgrade branch first"
            ;;
    esac
}

ensure_no_tracked_dirty_files() {
    local repo="$1"
    local dirty

    if $allow_dirty; then
        return
    fi

    dirty=$(git -C "$repo" status --short --untracked-files=no)
    if [[ -n "$dirty" ]]; then
        echo "$dirty" >&2
        fail "$(repo_name "$repo") has tracked working tree changes. Commit/stash them or pass --allow-dirty."
    fi
}

repo_wrapper_versions() {
    local repo="$1"
    local props version versions
    versions=""

    while IFS= read -r props; do
        version=$(wrapper_version_from_props "$props")
        [[ -n "$version" ]] || fail "Could not read Gradle version from $(repo_relative "$repo" "$props") in $(repo_name "$repo")"
        versions=$(append_unique_line "$versions" "$version")
    done < <(discover_wrapper_props "$repo")

    printf '%s' "$versions"
}

repo_needs_upgrade() {
    local repo="$1"
    local target="$2"
    local props version comparison wrapper_count

    wrapper_count=0

    while IFS= read -r props; do
        wrapper_count=$((wrapper_count + 1))
        version=$(wrapper_version_from_props "$props")
        [[ -n "$version" ]] || fail "Could not read Gradle version from $(repo_relative "$repo" "$props") in $(repo_name "$repo")"
        comparison=$(compare_gradle_versions "$target" "$version")
        if [[ "$comparison" == "-1" ]]; then
            fail "Refusing to downgrade $(repo_name "$repo") $(repo_relative "$repo" "$props") from ${version} to ${target}"
        fi
        if [[ "$comparison" == "1" ]]; then
            return 0
        fi
    done < <(discover_wrapper_props "$repo")

    [[ "$wrapper_count" -gt 0 ]] || fail "No Gradle wrapper properties found in $(repo_name "$repo")"
    return 1
}

ensure_upgrade_branch_available() {
    local repo="$1"
    local branch="$2"
    local current
    local ls_remote_status

    ensure_no_tracked_dirty_files "$repo"

    current=$(current_branch "$repo")
    [[ -n "$current" ]] || fail "$(repo_name "$repo") is in detached HEAD; refusing to create upgrade branch"

    if [[ "$current" == "$branch" ]]; then
        ensure_not_protected_branch "$repo"
        return
    fi

    if git -C "$repo" show-ref --verify --quiet "refs/heads/${branch}"; then
        fail "$(repo_name "$repo") already has local branch ${branch}; check it out or delete it before rerunning"
    fi

    git -C "$repo" remote get-url origin >/dev/null ||
        fail "$(repo_name "$repo") has no origin remote to push ${branch}"

    set +e
    git -C "$repo" ls-remote --exit-code --heads origin "$branch" >/dev/null 2>&1
    ls_remote_status=$?
    set -e

    case "$ls_remote_status" in
        0)
            fail "$(repo_name "$repo") already has origin/${branch}; use a new version branch or remove the remote branch"
            ;;
        2)
            ;;
        *)
            fail "Could not query origin for $(repo_name "$repo") branch ${branch}"
            ;;
    esac
}

create_upgrade_branch() {
    local repo="$1"
    local branch="$2"
    local current

    current=$(current_branch "$repo")
    if [[ "$current" == "$branch" ]]; then
        info "[$(repo_name "$repo")] Using existing branch ${branch}"
    else
        info "[$(repo_name "$repo")] Creating branch ${branch}"
        git -C "$repo" switch -c "$branch"
    fi

    ensure_not_protected_branch "$repo"
}

check_repo_consistency() {
    local repo="$1"
    local props version versions wrapper_count version_count project_creator template_version
    local settings_plugin_build expected_line exact_matches unexpected_matches line file

    versions=""
    wrapper_count=0

    while IFS= read -r props; do
        wrapper_count=$((wrapper_count + 1))
        version=$(wrapper_version_from_props "$props")
        [[ -n "$version" ]] || fail "Could not read Gradle version from $(repo_relative "$repo" "$props") in $(repo_name "$repo")"
        versions=$(append_unique_line "$versions" "$version")

        if ! grep -q "gradle-${version}-bin.zip" "$props"; then
            fail "$(repo_relative "$repo" "$props") must use the -bin distribution for Gradle $version"
        fi
    done < <(discover_wrapper_props "$repo")

    [[ "$wrapper_count" -gt 0 ]] || fail "No Gradle wrapper properties found in $(repo_name "$repo")"

    version_count=$(count_lines "$versions")
    if [[ "$version_count" -ne 1 ]]; then
        echo "$versions" >&2
        fail "$(repo_name "$repo") has multiple Gradle wrapper versions"
    fi
    version=$(printf '%s\n' "$versions" | head -1)

    project_creator="$repo/javatools/src/main/java/org/xvm/tool/XtcProjectCreator.java"
    if [[ -f "$project_creator" ]]; then
        template_version=$(sed -n 's|.*DEFAULT_GRADLE_VERSION = "\([^"]*\)".*|\1|p' "$project_creator" | tail -1)
        [[ -n "$template_version" ]] || fail "Could not read DEFAULT_GRADLE_VERSION from $(repo_relative "$repo" "$project_creator")"
        if [[ "$version" != "$template_version" ]]; then
            fail "$(repo_name "$repo") wrapper version ($version) does not match XtcProjectCreator.DEFAULT_GRADLE_VERSION ($template_version)"
        fi
    fi

    settings_plugin_build="$repo/build-logic/settings-plugins/build.gradle.kts"
    if [[ -f "$settings_plugin_build" ]]; then
        expected_line=$(gradle_line_for_version "$version")
        if ! grep -q "Gradle ${expected_line} embeds Kotlin" "$settings_plugin_build"; then
            fail "$(repo_relative "$repo" "$settings_plugin_build") does not document the current Gradle line (${expected_line})"
        fi
    fi

    exact_matches=$(git -C "$repo" grep -n -F "$version" -- . ':!**/build/**' ':!.gradle/**' || true)
    unexpected_matches=""

    while IFS= read -r line; do
        [[ -n "$line" ]] || continue
        file=${line%%:*}
        if [[ "$file" == */gradle/wrapper/gradle-wrapper.properties ]]; then
            continue
        fi
        if [[ "$file" == "gradle/wrapper/gradle-wrapper.properties" ]]; then
            continue
        fi
        if [[ "$file" == "javatools/src/main/java/org/xvm/tool/XtcProjectCreator.java" ]]; then
            continue
        fi
        unexpected_matches="${unexpected_matches}${line}"$'\n'
    done <<< "$exact_matches"

    if [[ -n "$unexpected_matches" ]]; then
        echo "$unexpected_matches" >&2
        fail "Unexpected exact Gradle version references found in $(repo_name "$repo") for $version"
    fi
}

rewrite_wrapper_properties() {
    local props="$1"
    local resolved_version="$2"
    local distribution_base distribution_path network_timeout retries retry_backoff validate_url
    local zip_store_base zip_store_path tmp_file

    distribution_base=$(read_property_file "$props" distributionBase)
    distribution_path=$(read_property_file "$props" distributionPath)
    network_timeout=$(read_property_file "$props" networkTimeout)
    retries=$(read_property_file "$props" retries)
    retry_backoff=$(read_property_file "$props" retryBackOffMs)
    validate_url=$(read_property_file "$props" validateDistributionUrl)
    zip_store_base=$(read_property_file "$props" zipStoreBase)
    zip_store_path=$(read_property_file "$props" zipStorePath)

    distribution_base=${distribution_base:-GRADLE_USER_HOME}
    distribution_path=${distribution_path:-wrapper/dists}
    network_timeout=${network_timeout:-10000}
    retries=${retries:-0}
    retry_backoff=${retry_backoff:-500}
    validate_url=${validate_url:-true}
    zip_store_base=${zip_store_base:-GRADLE_USER_HOME}
    zip_store_path=${zip_store_path:-wrapper/dists}

    tmp_file="${props}.tmp"
    cat > "$tmp_file" <<EOF
distributionBase=${distribution_base}
distributionPath=${distribution_path}
distributionUrl=https\\://services.gradle.org/distributions/gradle-${resolved_version}-bin.zip
networkTimeout=${network_timeout}
retries=${retries}
retryBackOffMs=${retry_backoff}
validateDistributionUrl=${validate_url}
zipStoreBase=${zip_store_base}
zipStorePath=${zip_store_path}
EOF
    mv "$tmp_file" "$props"
}

run_wrapper_task_twice() {
    local gradle_root="$1"
    local requested_version="$2"
    local network_timeout="$3"

    [[ -x "$gradle_root/gradlew" ]] || fail "$(repo_relative "$gradle_root" "$gradle_root/gradlew") is not executable"

    info "[$(basename "$gradle_root")] Running wrapper task with Gradle ${requested_version}"
    (
        cd "$gradle_root"
        ./gradlew --console=plain --warning-mode=summary wrapper \
            --gradle-version "$requested_version" \
            --distribution-type bin \
            --network-timeout "$network_timeout"
    )

    info "[$(basename "$gradle_root")] Running wrapper task again under target Gradle"
    (
        cd "$gradle_root"
        ./gradlew --console=plain --warning-mode=summary wrapper \
            --gradle-version "$requested_version" \
            --distribution-type bin \
            --network-timeout "$network_timeout"
    )
}

refresh_daemon_jvm_properties() {
    local repo="$1"
    local gradle_root="$2"
    local daemon_props="$gradle_root/gradle/gradle-daemon-jvm.properties"
    local jvm_version

    [[ -f "$daemon_props" ]] || return

    jvm_version=$(read_property_file "$daemon_props" toolchainVersion)
    [[ -n "$jvm_version" ]] || fail "Could not read toolchainVersion from $(repo_relative "$repo" "$daemon_props")"

    info "[$(repo_name "$repo")] Refreshing $(repo_relative "$repo" "$daemon_props") for JVM ${jvm_version}"
    (
        cd "$gradle_root"
        ./gradlew --console=plain --warning-mode=summary updateDaemonJvm \
            --jvm-version "$jvm_version"
    )
}

upgrade_wrapper_root() {
    local repo="$1"
    local props="$2"
    local target="$3"
    local gradle_root old_version resolved_version network_timeout comparison

    old_version=$(wrapper_version_from_props "$props")
    comparison=$(compare_gradle_versions "$target" "$old_version")
    if [[ "$comparison" == "0" ]]; then
        info "[$(repo_name "$repo")] $(repo_relative "$repo" "$props") already at Gradle ${target}"
        return
    fi

    gradle_root=$(gradle_root_for_props "$props")
    network_timeout=$(read_property_file "$props" networkTimeout)
    network_timeout=${network_timeout:-10000}

    info "[$(repo_name "$repo")] Upgrading $(repo_relative "$repo" "$gradle_root") from ${old_version} to ${target}"
    run_wrapper_task_twice "$gradle_root" "$target" "$network_timeout"

    resolved_version=$(wrapper_version_from_props "$props")
    [[ "$resolved_version" == "$target" ]] ||
        fail "$(repo_relative "$repo" "$props") resolved to Gradle ${resolved_version}, expected ${target}"

    rewrite_wrapper_properties "$props" "$resolved_version"
    refresh_daemon_jvm_properties "$repo" "$gradle_root"
}

update_project_creator_version() {
    local repo="$1"
    local resolved_version="$2"
    local project_creator="$repo/javatools/src/main/java/org/xvm/tool/XtcProjectCreator.java"

    [[ -f "$project_creator" ]] || return

    DEFAULT_GRADLE_VERSION="$resolved_version" perl -0pi -e \
        's/public static final String DEFAULT_GRADLE_VERSION = "[^"]+";/public static final String DEFAULT_GRADLE_VERSION = "$ENV{DEFAULT_GRADLE_VERSION}";/' \
        "$project_creator"
}

embedded_kotlin_version() {
    local repo="$1"
    (
        cd "$repo"
        ./gradlew --console=plain --warning-mode=summary --version
    ) | sed -n 's/^Kotlin:[[:space:]]*//p' | head -1
}

update_embedded_kotlin_comment() {
    local repo="$1"
    local resolved_version="$2"
    local settings_plugin_build="$repo/build-logic/settings-plugins/build.gradle.kts"
    local kotlin_version gradle_line

    [[ -f "$settings_plugin_build" ]] || return

    kotlin_version=$(embedded_kotlin_version "$repo")
    [[ -n "$kotlin_version" ]] || fail "Could not determine embedded Kotlin version in $(repo_name "$repo")"

    gradle_line=$(gradle_line_for_version "$resolved_version")
    GRADLE_LINE="$gradle_line" KOTLIN_VERSION="$kotlin_version" perl -0pi -e \
        's|// Gradle [^ ]+ embeds Kotlin [^,]+, which supports JVM target ([0-9]+)\.|// Gradle $ENV{GRADLE_LINE} embeds Kotlin $ENV{KOTLIN_VERSION}, which supports JVM target $1.|' \
        "$settings_plugin_build"
}

fail_on_stale_old_version() {
    local repo="$1"
    local old_version="$2"
    local target="$3"
    local stale

    [[ "$old_version" != "$target" ]] || return

    stale=$(git -C "$repo" grep -n -F "$old_version" -- . ':!**/build/**' ':!.gradle/**' || true)
    if [[ -n "$stale" ]]; then
        echo "$stale" >&2
        fail "Old Gradle version $old_version still appears in tracked files in $(repo_name "$repo")"
    fi
}

validation_task_for_root() {
    local repo="$1"
    local gradle_root="$2"

    if [[ "$gradle_root" == "$repo" && -d "$repo/javatools" ]]; then
        printf '%s\n' ':javatools:compileJava'
    else
        printf '%s\n' 'help'
    fi
}

run_root_validation() {
    local repo="$1"
    local gradle_root="$2"
    local task

    info "[$(repo_name "$repo")] Validating $(repo_relative "$repo" "$gradle_root") wrapper"
    (
        cd "$gradle_root"
        ./gradlew --console=plain --warning-mode=summary --version
    )

    if $skip_build_validation; then
        info "[$(repo_name "$repo")] Skipping build validation for $(repo_relative "$repo" "$gradle_root")"
        return
    fi

    task=$(validation_task_for_root "$repo" "$gradle_root")
    info "[$(repo_name "$repo")] Running validation task ${task}"
    (
        cd "$gradle_root"
        ./gradlew --console=plain --warning-mode=summary "$task"
    )
}

run_repo_validation() {
    local repo="$1"
    local props gradle_root

    while IFS= read -r props; do
        gradle_root=$(gradle_root_for_props "$props")
        run_root_validation "$repo" "$gradle_root"
    done < <(discover_wrapper_props "$repo")
}

upgrade_repo() {
    local repo="$1"
    local target="$2"
    local props old_version old_versions final_version

    old_versions=""

    ensure_not_protected_branch "$repo"

    while IFS= read -r props; do
        old_version=$(wrapper_version_from_props "$props")
        old_versions=$(append_unique_line "$old_versions" "$old_version")
        upgrade_wrapper_root "$repo" "$props" "$target"
    done < <(discover_wrapper_props "$repo")

    update_project_creator_version "$repo" "$target"
    update_embedded_kotlin_comment "$repo" "$target"

    while IFS= read -r old_version; do
        [[ -n "$old_version" ]] || continue
        fail_on_stale_old_version "$repo" "$old_version" "$target"
    done <<< "$old_versions"

    check_repo_consistency "$repo"

    final_version=$(repo_wrapper_versions "$repo" | head -1)
    [[ "$final_version" == "$target" ]] || fail "$(repo_name "$repo") did not finish at Gradle ${target}"

    run_repo_validation "$repo"
}

stage_gradle_upgrade_changes() {
    local repo="$1"
    local props gradle_root candidate rel

    while IFS= read -r props; do
        gradle_root=$(gradle_root_for_props "$props")
        for candidate in \
            "$gradle_root/gradle/wrapper/gradle-wrapper.properties" \
            "$gradle_root/gradle/wrapper/gradle-wrapper.jar" \
            "$gradle_root/gradle/gradle-daemon-jvm.properties" \
            "$gradle_root/gradlew" \
            "$gradle_root/gradlew.bat"
        do
            if [[ -e "$candidate" ]]; then
                rel=$(repo_relative "$repo" "$candidate")
                git -C "$repo" add -- "$rel"
            fi
        done
    done < <(discover_wrapper_props "$repo")

    for candidate in \
        "$repo/javatools/src/main/java/org/xvm/tool/XtcProjectCreator.java" \
        "$repo/build-logic/settings-plugins/build.gradle.kts" \
        "$repo/gradle/gradle-daemon-jvm.properties"
    do
        if [[ -e "$candidate" ]]; then
            rel=$(repo_relative "$repo" "$candidate")
            git -C "$repo" add -- "$rel"
        fi
    done
}

commit_and_push_repo() {
    local repo="$1"
    local target="$2"
    local branch="$3"
    local status

    status=$(git -C "$repo" status --short --untracked-files=no)
    if [[ -z "$status" ]]; then
        info "[$(repo_name "$repo")] No tracked Gradle upgrade changes to commit"
        return
    fi

    stage_gradle_upgrade_changes "$repo"

    if git -C "$repo" diff --cached --quiet; then
        echo "$status" >&2
        fail "$(repo_name "$repo") has tracked changes, but they were not in the Gradle upgrade staging set"
    fi

    info "[$(repo_name "$repo")] Committing Gradle upgrade"
    git -C "$repo" commit -m "Upgrade Gradle to ${target}"

    status=$(git -C "$repo" status --short --untracked-files=no)
    if [[ -n "$status" ]]; then
        echo "$status" >&2
        fail "$(repo_name "$repo") still has tracked changes after committing the Gradle upgrade"
    fi

    info "[$(repo_name "$repo")] Pushing ${branch}"
    git -C "$repo" push -u origin "$branch"
}

target_repos=$(discover_target_repos)

case "$mode" in
    check)
        [[ -z "$target_version" ]] || fail "--check does not take a version argument"
        while IFS= read -r repo; do
            [[ -n "$repo" ]] || continue
            check_repo_consistency "$repo"
            info "[$(repo_name "$repo")] Gradle wrapper metadata is consistent"
        done <<< "$target_repos"
        ;;
    upgrade)
        if [[ -z "$target_version" ]]; then
            target_version=$(resolve_latest_gradle_version)
            info "Latest stable Gradle version is ${target_version}"
        fi
        validate_version_argument "$target_version"

        upgrade_repos=""
        branch_name=$(upgrade_branch_name "$target_version")

        while IFS= read -r repo; do
            [[ -n "$repo" ]] || continue
            if repo_needs_upgrade "$repo" "$target_version"; then
                upgrade_repos=$(append_unique_line "$upgrade_repos" "$repo")
            fi
        done <<< "$target_repos"

        if [[ -z "$upgrade_repos" ]]; then
            while IFS= read -r repo; do
                [[ -n "$repo" ]] || continue
                check_repo_consistency "$repo"
            done <<< "$target_repos"
            echo "WARNING: All selected Gradle wrappers are already at ${target_version}; no upgrade performed." >&2
            exit 0
        fi

        if $uses_upgrade_branches; then
            while IFS= read -r repo; do
                [[ -n "$repo" ]] || continue
                ensure_upgrade_branch_available "$repo" "$branch_name"
            done <<< "$upgrade_repos"

            while IFS= read -r repo; do
                [[ -n "$repo" ]] || continue
                create_upgrade_branch "$repo" "$branch_name"
            done <<< "$upgrade_repos"
        else
            ensure_not_protected_branch "$repo_root"
            ensure_no_tracked_dirty_files "$repo_root"
        fi

        while IFS= read -r repo; do
            [[ -n "$repo" ]] || continue
            upgrade_repo "$repo" "$target_version"
        done <<< "$upgrade_repos"

        if $uses_upgrade_branches; then
            while IFS= read -r repo; do
                [[ -n "$repo" ]] || continue
                commit_and_push_repo "$repo" "$target_version" "$branch_name"
            done <<< "$upgrade_repos"
        fi

        info "Gradle upgrade complete for selected repos: ${target_version}"
        info "Review plugin compatibility separately when crossing Gradle minor/major lines, especially Develocity and Foojay."
        ;;
    *)
        fail "Unknown mode: $mode"
        ;;
esac
