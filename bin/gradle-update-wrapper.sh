#!/usr/bin/env bash
#
# Upgrade the repository Gradle wrapper and the repo-local Gradle version mirrors.
#
# This script intentionally owns more than gradle/wrapper/gradle-wrapper.properties:
# historical Gradle upgrade PRs have also needed regenerated wrapper artifacts,
# XtcProjectCreator's fallback Gradle version, and embedded-Kotlin comments.

set -euo pipefail

usage() {
    cat <<'EOF'
Usage:
  bin/gradle-update-wrapper.sh [gradle-version] [--skip-build-validation] [--allow-dirty]
  bin/gradle-update-wrapper.sh --check

Examples:
  bin/gradle-update-wrapper.sh
  bin/gradle-update-wrapper.sh 9.6.1
  bin/gradle-update-wrapper.sh 9.6.1 --skip-build-validation
  bin/gradle-update-wrapper.sh --check

When no version is provided, the script resolves the current stable Gradle
version from https://services.gradle.org/versions/current.

What the upgrade mode does:
  1. Runs the Gradle wrapper task twice so properties, wrapper jar, and scripts
     are generated by the target Gradle version.
  2. Rewrites gradle-wrapper.properties using the repository policy:
     services.gradle.org, -bin distribution, existing timeout/retry settings.
  3. Updates XtcProjectCreator.DEFAULT_GRADLE_VERSION.
  4. Updates the build-logic comment that records Gradle's embedded Kotlin.
  5. Fails if the old exact Gradle version remains in tracked files.
  6. Runs a focused build validation unless --skip-build-validation is used.

What --check does:
  Checks the current tree for stale Gradle version mirrors without changing files.
EOF
}

fail() {
    echo "ERROR: $*" >&2
    exit 1
}

info() {
    echo "==> $*" >&2
}

mode="upgrade"
target_version=""
skip_build_validation=false
allow_dirty=false

while [[ $# -gt 0 ]]; do
    case "$1" in
        --check)
            mode="check"
            shift
            ;;
        --skip-build-validation)
            skip_build_validation=true
            shift
            ;;
        --allow-dirty)
            allow_dirty=true
            shift
            ;;
        -h|--help)
            usage
            exit 0
            ;;
        --)
            shift
            break
            ;;
        -*)
            fail "Unknown option: $1"
            ;;
        *)
            if [[ -n "$target_version" ]]; then
                fail "Unexpected extra argument: $1"
            fi
            target_version="$1"
            shift
            ;;
    esac
done

repo_root=$(git rev-parse --show-toplevel)
cd "$repo_root"

wrapper_props="gradle/wrapper/gradle-wrapper.properties"
project_creator="javatools/src/main/java/org/xvm/tool/XtcProjectCreator.java"
settings_plugin_build="build-logic/settings-plugins/build.gradle.kts"
policy_distribution_base=""
policy_distribution_path=""
policy_network_timeout=""
policy_retries=""
policy_retry_backoff=""
policy_validate_url=""
policy_zip_store_base=""
policy_zip_store_path=""

[[ -f "$wrapper_props" ]] || fail "$wrapper_props not found"
[[ -f "$project_creator" ]] || fail "$project_creator not found"

read_property() {
    local key="$1"
    awk -F= -v key="$key" '$1 == key {print substr($0, index($0, "=") + 1)}' "$wrapper_props" | tail -1
}

current_wrapper_version() {
    sed -n 's|^distributionUrl=.*gradle-\(.*\)-bin\.zip.*|\1|p' "$wrapper_props" | tail -1
}

current_template_version() {
    sed -n 's|.*DEFAULT_GRADLE_VERSION = "\([^"]*\)".*|\1|p' "$project_creator" | tail -1
}

gradle_line_for_version() {
    local version="$1"
    local major minor

    major=$(printf '%s' "$version" | sed -n 's/^\([0-9][0-9]*\).*/\1/p')
    minor=$(printf '%s' "$version" | sed -n 's/^[0-9][0-9]*\.\([0-9][0-9]*\).*/\1/p')
    if [[ -n "$major" && -n "$minor" ]]; then
        printf '%s.%s.x' "$major" "$minor"
    else
        printf '%s' "$version"
    fi
}

validate_version_argument() {
    local version="$1"
    [[ -n "$version" ]] || fail "Gradle version is required"
    [[ "$version" != *[[:space:]]* ]] || fail "Gradle version must not contain whitespace"
    [[ "$version" =~ ^[0-9]+\.[0-9]+\.[0-9]+$ ]] || fail "Use a stable numeric Gradle version like 9.6.1"
}

resolve_latest_gradle_version() {
    local payload version

    command -v curl >/dev/null 2>&1 || fail "curl is required to resolve the latest Gradle version"

    info "Resolving latest stable Gradle version"
    payload=$(curl -fsSL https://services.gradle.org/versions/current) ||
        fail "Could not resolve latest Gradle version from services.gradle.org"
    version=$(printf '%s\n' "$payload" | sed -n 's/.*"version"[[:space:]]*:[[:space:]]*"\([^"]*\)".*/\1/p' | head -1)
    [[ -n "$version" ]] || fail "Could not parse latest Gradle version from services.gradle.org response"

    printf '%s\n' "$version"
}

compare_gradle_versions() {
    local left="$1"
    local right="$2"
    local left_major left_minor left_patch right_major right_minor right_patch

    IFS=. read -r left_major left_minor left_patch <<< "$left"
    IFS=. read -r right_major right_minor right_patch <<< "$right"

    left_patch=${left_patch:-0}
    right_patch=${right_patch:-0}

    if (( left_major < right_major )); then
        printf '%s\n' -1
    elif (( left_major > right_major )); then
        printf '%s\n' 1
    elif (( left_minor < right_minor )); then
        printf '%s\n' -1
    elif (( left_minor > right_minor )); then
        printf '%s\n' 1
    elif (( left_patch < right_patch )); then
        printf '%s\n' -1
    elif (( left_patch > right_patch )); then
        printf '%s\n' 1
    else
        printf '%s\n' 0
    fi
}

ensure_no_tracked_dirty_files() {
    if $allow_dirty; then
        return
    fi

    local dirty
    dirty=$(git status --short --untracked-files=no)
    if [[ -n "$dirty" ]]; then
        echo "$dirty" >&2
        fail "Tracked working tree changes exist. Commit/stash them or pass --allow-dirty."
    fi
}

check_consistency() {
    local wrapper_version template_version expected_line exact_matches unexpected_matches

    wrapper_version=$(current_wrapper_version)
    [[ -n "$wrapper_version" ]] || fail "Could not read Gradle version from $wrapper_props"

    template_version=$(current_template_version)
    [[ -n "$template_version" ]] || fail "Could not read DEFAULT_GRADLE_VERSION from $project_creator"

    if [[ "$wrapper_version" != "$template_version" ]]; then
        fail "Gradle wrapper version ($wrapper_version) does not match XtcProjectCreator.DEFAULT_GRADLE_VERSION ($template_version)"
    fi

    if ! grep -q "gradle-${wrapper_version}-bin.zip" "$wrapper_props"; then
        fail "$wrapper_props must use the -bin distribution for Gradle $wrapper_version"
    fi

    if [[ -f "$settings_plugin_build" ]]; then
        expected_line=$(gradle_line_for_version "$wrapper_version")
        if ! grep -q "Gradle ${expected_line} embeds Kotlin" "$settings_plugin_build"; then
            fail "$settings_plugin_build does not document the current Gradle line (${expected_line})"
        fi
    fi

    exact_matches=$(git grep -n -F "$wrapper_version" -- . ':!**/build/**' ':!.gradle/**' || true)
    unexpected_matches=$(printf '%s\n' "$exact_matches" |
        grep -v '^gradle/wrapper/gradle-wrapper.properties:' |
        grep -v '^javatools/src/main/java/org/xvm/tool/XtcProjectCreator.java:' || true)

    if [[ -n "$unexpected_matches" ]]; then
        echo "$unexpected_matches" >&2
        fail "Unexpected exact Gradle version references found for $wrapper_version"
    fi
}

capture_wrapper_policy() {
    policy_distribution_base=$(read_property distributionBase)
    policy_distribution_path=$(read_property distributionPath)
    policy_network_timeout=$(read_property networkTimeout)
    policy_retries=$(read_property retries)
    policy_retry_backoff=$(read_property retryBackOffMs)
    policy_validate_url=$(read_property validateDistributionUrl)
    policy_zip_store_base=$(read_property zipStoreBase)
    policy_zip_store_path=$(read_property zipStorePath)

    policy_distribution_base=${policy_distribution_base:-GRADLE_USER_HOME}
    policy_distribution_path=${policy_distribution_path:-wrapper/dists}
    policy_network_timeout=${policy_network_timeout:-10000}
    policy_retries=${policy_retries:-0}
    policy_retry_backoff=${policy_retry_backoff:-500}
    policy_validate_url=${policy_validate_url:-true}
    policy_zip_store_base=${policy_zip_store_base:-GRADLE_USER_HOME}
    policy_zip_store_path=${policy_zip_store_path:-wrapper/dists}
}

rewrite_wrapper_properties() {
    local resolved_version="$1"
    local tmp_file

    if [[ -z "$policy_distribution_base" ]]; then
        capture_wrapper_policy
    fi

    tmp_file="${wrapper_props}.tmp"
    cat > "$tmp_file" <<EOF
distributionBase=${policy_distribution_base}
distributionPath=${policy_distribution_path}
distributionUrl=https\\://services.gradle.org/distributions/gradle-${resolved_version}-bin.zip
networkTimeout=${policy_network_timeout}
retries=${policy_retries}
retryBackOffMs=${policy_retry_backoff}
validateDistributionUrl=${policy_validate_url}
zipStoreBase=${policy_zip_store_base}
zipStorePath=${policy_zip_store_path}
EOF
    mv "$tmp_file" "$wrapper_props"
}

update_project_creator_version() {
    local resolved_version="$1"
    DEFAULT_GRADLE_VERSION="$resolved_version" perl -0pi -e \
        's/public static final String DEFAULT_GRADLE_VERSION = "[^"]+";/public static final String DEFAULT_GRADLE_VERSION = "$ENV{DEFAULT_GRADLE_VERSION}";/' \
        "$project_creator"
}

embedded_kotlin_version() {
    ./gradlew --console=plain --warning-mode=summary --version |
        sed -n 's/^Kotlin:[[:space:]]*//p' |
        head -1
}

update_embedded_kotlin_comment() {
    local resolved_version="$1"
    local kotlin_version gradle_line

    [[ -f "$settings_plugin_build" ]] || return

    kotlin_version=$(embedded_kotlin_version)
    [[ -n "$kotlin_version" ]] || fail "Could not determine embedded Kotlin version from ./gradlew --version"

    gradle_line=$(gradle_line_for_version "$resolved_version")
    GRADLE_LINE="$gradle_line" KOTLIN_VERSION="$kotlin_version" perl -0pi -e \
        's|// Gradle [^ ]+ embeds Kotlin [^,]+, which supports JVM target ([0-9]+)\.|// Gradle $ENV{GRADLE_LINE} embeds Kotlin $ENV{KOTLIN_VERSION}, which supports JVM target $1.|' \
        "$settings_plugin_build"
}

fail_on_stale_old_version() {
    local old_version="$1"
    local resolved_version="$2"
    local stale

    [[ "$old_version" != "$resolved_version" ]] || return

    stale=$(git grep -n -F "$old_version" -- . ':!**/build/**' ':!.gradle/**' || true)
    if [[ -n "$stale" ]]; then
        echo "$stale" >&2
        fail "Old Gradle version $old_version still appears in tracked files"
    fi
}

run_wrapper_task_twice() {
    local requested_version="$1"
    local network_timeout

    network_timeout=$(read_property networkTimeout)
    network_timeout=${network_timeout:-10000}

    info "Running wrapper task with Gradle version ${requested_version}"
    ./gradlew --console=plain --warning-mode=summary wrapper \
        --gradle-version "$requested_version" \
        --distribution-type bin \
        --network-timeout "$network_timeout"

    info "Running wrapper task again so jar/scripts come from the target Gradle version"
    ./gradlew --console=plain --warning-mode=summary wrapper \
        --gradle-version "$requested_version" \
        --distribution-type bin \
        --network-timeout "$network_timeout"
}

run_build_validation() {
    info "Validating upgraded wrapper"
    ./gradlew --console=plain --warning-mode=summary --version

    if ! $skip_build_validation; then
        info "Running focused build validation"
        ./gradlew --console=plain --warning-mode=summary :javatools:compileJava
    else
        info "Skipping focused build validation"
    fi
}

case "$mode" in
    check)
        [[ -z "$target_version" ]] || fail "--check does not take a version argument"
        check_consistency
        info "Gradle wrapper metadata is consistent"
        ;;
    upgrade)
        if [[ -z "$target_version" ]]; then
            target_version=$(resolve_latest_gradle_version)
            info "Latest stable Gradle version is ${target_version}"
        fi
        validate_version_argument "$target_version"

        old_version=$(current_wrapper_version)
        [[ -n "$old_version" ]] || fail "Could not read current Gradle version"

        comparison=$(compare_gradle_versions "$target_version" "$old_version")
        if [[ "$comparison" == "-1" ]]; then
            fail "Refusing to downgrade Gradle from ${old_version} to ${target_version}"
        fi
        if [[ "$comparison" == "0" ]]; then
            echo "WARNING: Gradle is already at ${old_version}; no upgrade performed." >&2
            exit 0
        fi

        ensure_no_tracked_dirty_files
        capture_wrapper_policy

        info "Upgrading Gradle wrapper from ${old_version} to ${target_version}"
        run_wrapper_task_twice "$target_version"

        resolved_version=$(current_wrapper_version)
        [[ -n "$resolved_version" ]] || fail "Could not read resolved Gradle version after wrapper task"

        rewrite_wrapper_properties "$resolved_version"
        update_project_creator_version "$resolved_version"
        update_embedded_kotlin_comment "$resolved_version"
        fail_on_stale_old_version "$old_version" "$resolved_version"
        check_consistency
        run_build_validation

        info "Gradle upgrade complete: ${old_version} -> ${resolved_version}"
        info "Review plugin compatibility separately when crossing Gradle minor/major lines, especially Develocity and Foojay."
        ;;
    *)
        fail "Unknown mode: $mode"
        ;;
esac
